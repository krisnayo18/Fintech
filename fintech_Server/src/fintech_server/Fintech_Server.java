        User user = new User();
        double invessUSD = Double.parseDouble(jTextFieldInvesUsd.getText());
        double invessEURO = Double.parseDouble(jTextFieldInvesEuro.getText());
        double invessPOUNDS = Double.parseDouble(jTextFieldInvesPounds.getText());
