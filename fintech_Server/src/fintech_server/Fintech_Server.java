        double bacaPOUNDS = Double.parseDouble(jTextFieldBacaPounds.getText());
        double bacaIHSG = Double.parseDouble(jTextFieldBacaIhsg.getText());
        user.insertSaham("Baca", bacaUSD , bacaEURO, bacaPOUNDS, bacaIHSG);   
